    public void logar() {

        String sql = "select * from tbusuarios where login = ? and senha = ?";
        try {
            //as linhas abaixo preparam a consulta ao BD em função do que foi digitado nas caixas de texto, o ? é substituido pelo conteúdo das variáveis 
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtUsuario.getText());
            String captura = new String(txtSenha.getPassword());
            pst.setString(2, captura);
            //a linha abaixo xecuta a query
            rs = pst.executeQuery();
            //se existir usuário de senha correspondente
            if (rs.next()) {
                TelaPrincipal principal = new TelaPrincipal();
                principal.setVisible(true);
                this.dispose();
                conexao.close();
            } else {
                JOptionPane.showMessageDialog(null, "Usuário e/ou senha inválido(a)");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

